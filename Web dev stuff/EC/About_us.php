  <h3>About us</h3>   
   The Office of her majesty's most honorable and loyal electoal commision in an independent body within the UK government, it was founded in
   2001 as a result of the Political Parties, Elections and Referendums Act (2000) which established the office as an administrative and oversight organisation for the UK's ellections.<br><br>
   The commision was empowered into it's current form following the introduction of the modern voting act (2021). We are still responsible for administration and oversight
   but now we also responsible for the provision and testing of voting equipment, policing electoral campaigns (with the assistence of the home office) 
   and conducting investigations into potential breaches of electoral law.<br><br>
   
   <img src ="Assets/Images/Office.jpg" style="width:60%;height:45%;"></a><br><br>
   
   The commision currently has around 200 staff, it is lead by an independent board of directors apointed by both houses of parliament and the independent apointments commision.<br>
   Our staff includes almost 100 highly skilled administrators who help set up and co-ordinate elections, we also have proffesional overseers, provisioning experts, technicians and investigators on staff.<br>
   Everyone at the electoral commision is dedicated to the work of keeping the UK's democracy fair and running smoothly.<br><br>
